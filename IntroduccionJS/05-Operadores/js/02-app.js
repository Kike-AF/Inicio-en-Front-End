const numero1 = 20;
const numero2 = "20";
const numero3 = 30;

// Revisar si 2 valores son iguales o diferentes
console.log(numero1 == numero3);
console.log(numero1 == numero2);

// Typeoff
console.log(typeof numero1);
console.log(typeof numero2);

// Operador estricto (revisa valor y tipo de dato)
console.log(numero1 === numero2);

// Diferenta a ...
console.log(numero1 != numero3);
console.log(numero1 != numero2);
console.log(numero1 !== numero2);