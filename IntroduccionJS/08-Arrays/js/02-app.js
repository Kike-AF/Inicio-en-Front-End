//Elementos de un arreglo
const numeros = [10, 20, 30, 40, 50];


//La forma más común de acceder a un elemento es por su índice
// El índice comienza en 0
console.log(numeros[0]); // 10
console.log(numeros[1]); // 20
console.log(numeros[2]); // 30
console.log(numeros[3]); // 40
console.log(numeros[20]); // undefined (no existe un elemento en esa posición)
